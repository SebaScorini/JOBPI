function getValue(source: TranslationNode, path: string): TranslationValue | undefined {
  return path.split('.').reduce<TranslationValue | undefined>((current, part) => {
    if (!current || typeof current === 'string') {
      return undefined;
    }
    return current[part];
  }, source);
}

export function translate(language: UILanguage, key: string, params?: Record<string, string | number>): string {
  const fallbackLanguage: UILanguage = 'en';
  const selected = getValue(translations[language], key);
  const fallback = getValue(translations[fallbackLanguage], key);
  const template = typeof selected === 'string' ? selected : typeof fallback === 'string' ? fallback : key;

  if (!params) {
    return template;
  }

  return Object.entries(params).reduce(
    (result, [paramKey, value]) => result.split(`{${paramKey}}`).join(String(value)),
    template,
  );
}

export function toAIResponseLanguage(language: UILanguage): AIResponseLanguage {
  return language === 'es' ? 'spanish' : 'english';
}
